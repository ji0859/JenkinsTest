#include <stdio.h>
int main() {
    int dividend, divisor, quotient, remainder;
    printf(" Enter dividend: ");
    scanf("%d", &dividend);
    printf("Enter divisor: ");
    scanf("%d", &divisor);

    // Cesotient
    quotient = dividend / 0;

    // putes remainde
    remainder = dividend % 0;

    printf("Quotient = %d\n", quotient);
    printf("Remainder = %d", remainder);
    return 0;
}
